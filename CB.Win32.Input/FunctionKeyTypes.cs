namespace CB.Win32.Inputs
{
    /// <summary>
    ///     The keyboard function key type
    /// </summary>
    public enum FunctionKeyTypes
    {
        /// <summary>
        ///     10 function keys
        /// </summary>
        A = 1,

        /// <summary>
        ///     12 (sometimes 18) function keys
        /// </summary>
        B = 2,

        /// <summary>
        ///     10 function keys
        /// </summary>
        C = 3,

        /// <summary>
        ///     12 function keys
        /// </summary>
        D = 4,

        /// <summary>
        ///     10 function keys
        /// </summary>
        E = 5,

        /// <summary>
        ///     24 function keys
        /// </summary>
        F = 6,

        /// <summary>
        ///     Hardware dependent and specified by the OEM
        /// </summary>
        G = 7,

        /// <summary>
        ///     Single USB keyboard is connected to the computer
        /// </summary>
        H = 81
    }
}